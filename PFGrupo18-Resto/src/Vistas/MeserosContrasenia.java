    private void jbCambiarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbCambiarActionPerformed
        MeseroData md = new MeseroData();
        String contra = jtfContraNueva.getText();
        if(contra.length() < 3 || contra.length() > 8) {
            JOptionPane.showMessageDialog(this, "La contraseña debe tener entre 3 a 8 caracteres.");
        } else {
            md.actualizarContrasenia(id_mesero, contra);
            this.dispose();
        }
    }//GEN-LAST:event_jbCambiarActionPerformed

